let currentLang = localStorage.getItem("dyners_lang") || "tr";

// Apply active translations across the document
function applyTranslations() {
  const elements = document.querySelectorAll("[data-translate]");
  elements.forEach(el => {
    const key = el.getAttribute("data-translate");
    if (translations[currentLang] && translations[currentLang][key]) {
      el.textContent = translations[currentLang][key];
    }
  });

  // Update URL active states or switcher label
  const labelEl = document.querySelector(".lang-switcher .current-lang .lang");
  if (labelEl) {
    labelEl.textContent = currentLang === "tr" ? "Türkçe" : "English";
  }
  
  // HTML tag lang update
  document.documentElement.setAttribute("lang", currentLang);
}

// Set active language
function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("dyners_lang", lang);
  applyTranslations();
}

// ==========================================
// DOCUMENT READY CONTROLLER
// ==========================================
document.addEventListener("DOMContentLoaded", () => {
  // 1. Initialize Translation System
  applyTranslations();

  // 2. Language Switcher Toggles
  const switcher = document.querySelector(".lang-switcher");
  if (switcher) {
    switcher.addEventListener("click", (e) => {
      e.stopPropagation();
      switcher.classList.toggle("open");
    });

    const options = switcher.querySelectorAll(".other-langs li");
    options.forEach(opt => {
      opt.addEventListener("click", () => {
        const val = opt.getAttribute("value");
        setLanguage(val);
      });
    });

    // Close switcher dropdown on click outside
    document.addEventListener("click", () => {
      switcher.classList.remove("open");
    });
  }

  // 3. Mobile Hamburger Menu Toggle
  const mobileBtn = document.querySelector(".mobile-btn");
  const mobileMenu = document.querySelector(".mobile-menu");
  const mobileLayer = document.querySelector(".mobile-menu-layer");
  const mobileClose = document.querySelector(".mobile-menu .close-btn");

  if (mobileBtn && mobileMenu) {
    const toggleMenu = () => {
      mobileBtn.classList.toggle("open");
      mobileMenu.classList.toggle("open");
    };

    mobileBtn.addEventListener("click", toggleMenu);
    if (mobileLayer) mobileLayer.addEventListener("click", toggleMenu);
    if (mobileClose) mobileClose.addEventListener("click", toggleMenu);

    // Close mobile menu when a nav link is clicked
    const links = mobileMenu.querySelectorAll("a");
    links.forEach(l => {
      l.addEventListener("click", () => {
        mobileBtn.classList.remove("open");
        mobileMenu.classList.remove("open");
      });
    });
  }

  // 4. Product Swiper Carousel (Home Page)
  const navItems = document.querySelectorAll(".product-nav-item");
  const imageGroups = document.querySelectorAll(".product-image-group");
  const infoGroups = document.querySelectorAll(".product-info-group");

  if (navItems.length > 0) {
    navItems.forEach(item => {
      item.addEventListener("click", () => {
        const targetIndex = item.getAttribute("data-index");

        // Remove active class from all navigation items
        navItems.forEach(nav => nav.classList.remove("active"));
        // Add active class to clicked item
        item.classList.add("active");

        // Switch active images
        imageGroups.forEach(imgGroup => {
          if (imgGroup.getAttribute("data-index") === targetIndex) {
            imgGroup.classList.add("active");
          } else {
            imgGroup.classList.remove("active");
          }
        });

        // Switch active descriptions
        infoGroups.forEach(infoGroup => {
          if (infoGroup.getAttribute("data-index") === targetIndex) {
            infoGroup.classList.add("active");
          } else {
            infoGroup.classList.remove("active");
          }
        });
      });
    });
  }

// 5. Teaser Video Player Modal
const videoCaller = document.getElementById("home-video-caller");
if (videoCaller) {
  // Add play button element inside if not present
  if (!videoCaller.querySelector(".play-btn")) {
    const playBtn = document.createElement("div");
    playBtn.className = "play-btn";
    videoCaller.appendChild(playBtn);
  }

  videoCaller.addEventListener("click", () => {
    // Gourmet stock burger video (Mixkit)
    const videoUrl = "https://assets.mixkit.co/videos/preview/mixkit-close-up-of-a-gourmet-hamburger-4433-large.mp4";

    const modal = document.createElement("div");
    modal.className = "video-overlay-modal";
    modal.innerHTML = `
      <div class="video-modal-container">
        <span class="video-modal-close">&times;</span>
        <video src="${videoUrl}" autoplay controls style="width:100%; height:100%; object-fit:cover;"></video>
      </div>
    `;

    document.body.appendChild(modal);
    // Trigger enter transition (needs a frame so CSS transition fires)
    requestAnimationFrame(() => modal.classList.add("active"));

    // Lock scroll
    document.body.style.overflow = "hidden";

    // Close handlers
    const closeModal = () => {
      modal.classList.remove("active");
      setTimeout(() => {
        modal.remove();
        document.body.style.overflow = "";
      }, 300);
    };

    modal.querySelector(".video-modal-close").addEventListener("click", closeModal);
    modal.addEventListener("click", (e) => {
      if (e.target === modal) closeModal();
    });
  });
}

  // 6. Scroll Parallax Assembly Engine (Home Page)
  const parallaxSection = document.getElementById("parallax-section");
  if (parallaxSection) {
    const pImages = parallaxSection.querySelectorAll(".p-image");
    
    const assembleBurger = () => {
      const rect = parallaxSection.getBoundingClientRect();
      const viewHeight = window.innerHeight;
      
      // Calculate how close the center of the parallax section is to the center of the viewport
      const viewportCenter = viewHeight / 2;
      const sectionCenter = rect.top + rect.height / 2;
      
      // Distance from screen center
      const distance = sectionCenter - viewportCenter;

      // Only animate if the section is currently visible in the viewport
      if (rect.top < viewHeight && rect.bottom > 0) {
        pImages.forEach(img => {
          const datatype = parseFloat(img.getAttribute("datatype"));
          if (!isNaN(datatype)) {
            // Formula to separate pieces on scrolling up/down
            // The multiply coefficient (0.22) determines separation sensitivity
            const offset = distance * datatype * 0.22;
            img.style.transform = `translateX(-50%) translateY(${offset}px)`;
          }
        });
      }
    };

    // Run once on load and bind scroll
    assembleBurger();
    window.addEventListener("scroll", assembleBurger);
  }
});
